package co.edu.uptc.model;

/**
 * Enum representing the possible categories for a product.
 */
public enum Category {
    /** Clothing products */
    ROPA,

    /** Food products */
    COMIDA,

    /** Medical or pharmaceutical products */
    MEDICAMENTO,

    /** Hygiene and personal care products */
    HIGIENE,

    /** Electronic devices and gadgets */
    ELECTRONICA,

    /** Household items and appliances */
    HOGAR,

    /** Toys and recreational items */
    JUGUETES,

    /** Stationery and bookstore items */
    LIBRERIA,

    /** Other uncategorized products */
    OTROS
}
